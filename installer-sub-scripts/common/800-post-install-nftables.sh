#!/bin/bash

# ------------------------------------------------------------------------------
# POST_INSTALL_NFTABLES.SH
# ------------------------------------------------------------------------------
set -e
source $INSTALLER/000-source

# ------------------------------------------------------------------------------
# INIT
# ------------------------------------------------------------------------------
[[ "$DONT_RUN_POST_INSTALL" = true ]] && exit

# ------------------------------------------------------------------------------
# NFTABLES
# ------------------------------------------------------------------------------
# recreate nftables.conf
cat <<EOF > /etc/nftables.conf
#!/usr/sbin/nft -f

flush ruleset

EOF

# save ruleset
nft -n list ruleset >> /etc/nftables.conf
